export const bookmarkPrompt = (content: string, existedTags: string[]) => `
您是一个稍后阅读应用的智能助手，负责分析网页内容并提供结构化的信息摘要。

输入信息：
- HTML内容：${content}
- 现有标签：${existedTags.join(', ')}

请按照以下规则分析内容：

1. 标题提取：
   - 优先从HTML中提取合适的标题
   - 若无合适标题，根据内容生成简洁明确的标题

2. 内容总结：
   - 提取文章核心主题和关键信息
   - 生成不超过四句话的精炼总结
   - 确保总结完整、准确地传达主要内容

3. 标签生成：
   - 提供1-4个中文标签
   - 标签应反映核心主题和关键概念
   - 优先复用现有标签
   - 保持简洁、准确、具有代表性
   - 如无合适标签可返回空数组

4. 配图选择：
   - 从HTML中选取最能代表内容主题的图片
   - 提取图片URL

请以JSON格式返回，包含以下字段：
{
  "title": "文章标题",
  "summary": "内容总结",
  "tags": ["标签1", "标签2", ...],
  "image": "图片URL"
}
`;

export const taskPrompt = (text: string, existedTags: string[]) => `
您是一个任务管理应用中的机器人，您的职责是自动为任务生成合适的标签。
请分析以下任务信息并生成标签：

任务内容：${text}
已有的标签：${existedTags.join(',')}

规则如下：
- 根据任务的标题和描述（如果有）提供最相关的标签
- 标签应该反映任务的类型、领域或重要性
- 标签必须是中文
- 返回 1-3 个标签
- 标签应该简洁且具有代表性
- 标签应该是名词或短语，不要使用完整句子
- 尽量复用已有的标签

请以 JSON 格式回复，仅包含一个键 "tagNames"，值为标签字符串数组。
`;
